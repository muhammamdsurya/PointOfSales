    public void tabel() { 
        data = new DefaultTableModel (new String[] { 
            "Tanggal",
            "Kasir", 
            "Nama Barang",
            "Qty",
            "Subtotal",
            "Total",
            
        }, 0);
        
        rs = conn.selectSlPr();
        try {
            while (rs.next() ) {
                
                data.addRow(new Object[] { 
                    rs.getString("date"),
                    rs.getString("kasir"),
                    rs.getString("nama"),
                    rs.getString("qty"),
                    rs.getString("subtotal"),
                    rs.getString("total"),
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(Laporan.class.getName()).log(Level.SEVERE, null, ex);
        }
        tabel.setModel(data);
    }
