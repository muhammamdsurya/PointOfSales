    public void tabel1(){ 
        data = new DefaultTableModel (new String[] {
            "Tanggal",
            "Kasir",
            "Total Penjualan",
        },0);
        
        rs = conn.selectSl();
        try {
            while (rs.next() ) {
                data.addRow(new Object[] {
                    rs.getString("date"),
                    rs.getString("kasir"),
                    rs.getString("total" ),
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(Laporan.class.getName()).log(Level.SEVERE, null, ex);
        }
         tabel1.setModel(data);
    }
